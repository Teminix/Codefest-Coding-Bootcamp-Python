# Loops are bits of code that can be executed repetitively
# While loop will run some code as long as a condition is satisfied


# The below code will print all numbers from 0 to 9
x = 0
while x < 10: # The condition is checked over and over until the condition is false
	print(x)  # It will keep executing the code that is in here

# Below code will print out items in a list
myList = [9,8,234,False] # Declare list with certain items in them
index = 0 # Declare variable named 'index'
while index < len(myList):
	print(myList[index]) # Print the value at the current index
	index = index + 1






